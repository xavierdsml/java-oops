package loopingStatement;

public class doWhileLoop {
    static void main() {
        int a = 1;
        do{
            System.out.print("Number greater than the 0 are print only : ");
            System.out.println(a);
            a--;
        }while(a>0);
    }
    public static void main(String[] args) {
        main();
    }
}